"""Serve the study shelf with a guide chat panel.

Binds 127.0.0.1 only. On startup the shelf page is regenerated by importing
the sibling build_shelf.py by path (importlib, the same pattern the tests
use) rather than shelling out through `uv run` — build_shelf is stdlib-only,
so importing it is fast, in-process, and fails loudly if it breaks.

Routes:
    GET  /          the shelf page (library/shelf.html, freshly rebuilt)
    GET  /health    {"ok": true, "brain": <configured>, "default_brain":
                    <where a no-pick request lands right now — hermes when
                    wired, else claude>, "brains": {...}, "ax": {"wired":
                    bool}, "prep_cron": {...}|null}. brains maps
                    claude/ollama to availability booleans and hermes to
                    an object: {"wired", "reason", "profile", "model",
                    "routes": [{"alias", "model"}...]} — the begin-here
                    machinery card and the brain pills read this.
    POST /api/chat  {"messages": [...], "brain"?, "session"?, "view"?,
                    "model"?} -> streamed reply. "brain" is optional and
                    per-request (the panel's toggle); with no pick the turn
                    lands on default_brain (hermes is the home harness when
                    the gate is open), unknown names are a 400.
                    "session" picks the episode ("new" starts fresh — new
                    claude thread too; default: the current one, which may
                    quietly ROLL OVER onto a fresh episode after
                    ROLLOVER_IDLE of silence — sessions are invisible
                    compaction now, one continuous guide as far as the
                    user can tell; explicit ids, e.g. the aX bridge's
                    per-sender episodes, are never rolled). "view" is the
                    talk slug open on the shelf, or null — it becomes
                    ambient context (see below). "model" (ollama brain
                    only) picks which installed local model answers —
                    validated against /api/tags (unknown → 400),
                    remembered in state.json as the default thereafter.
                    The X-Session response header names the episode that
                    recorded the turn.
    GET  /api/sessions  {"sessions": [{id,title,summary,updated}...]}, newest
                    first — kept for tooling; the panel no longer shows it.
    GET  /api/version  {"shelf_mtime"} — the page polls it and refreshes
                    itself (or offers to) when the shelf was rebuilt.
    POST /api/listened  {"slug"} — the page reports a completed listen;
                    appended to library/.listening.jsonl (deduped within
                    the hour) and to the talk's notes.md under
                    "## Listening". Feeds the card's "listened ✓" state
                    and the ambient wrap-up bridge.
    POST /api/unheard  {"slug"} — "mark unheard — come back to this":
                    appends a retraction {slug, at, retract: true} to the
                    same append-only log (latest entry wins), annotates
                    the talk's notes ("flagged to revisit"), rebuilds.
                    The path (done) state is untouched.
    POST /api/done  {"slug"} — "✓ Done with this talk", server-first:
                    records the listen if absent, moves the talk from
                    Queued to Studied on STUDY.md with a "(done for now —
                    not yet discussed)" one-liner, rebuilds the shelf.
                    Instant, idempotent, no LLM — the guide's follow-up
                    ("what's next") is a separate queued chat message.
    POST /api/reopen  {"slug"} — the exact inverse: Studied → Queued
                    with a "(reopened <date>)" one-liner + rebuild.
                    Reopening a not-done talk is a clean no-op.
    POST /api/chat/stop  {"session"?} — abort that episode's in-flight
                    turn (default: the current episode): the claude
                    subprocess is killed / the hermes or ollama stream
                    is closed; the partial reply stays in history ending
                    with a quiet "— stopped —" line. 404 when nothing is
                    streaming.
    GET  /api/models  installed Ollama models for the settings picker:
                    {"models": [{name, tools, size_gb}...], "current": name}
                    (tools per /api/show capabilities; 503 when Ollama is
                    down).
    GET  /api/prep  the ONE nightly-prep job, live from the gateway's jobs
                    API behind the wired-gate: {"wired", "found", "id"?,
                    "job"?: {name, schedule, model, provider, enabled}}.
                    Narrow by design — no other job, never the prompt.
    POST /api/prep/{run|pause|resume}  the same job's three controls,
                    proxied with the server-side gateway key (the page
                    never sees it). Unwired/missing job/refusal are clear
                    JSON {"error"} answers (503/404/502).
    GET  /artifacts/{slug}/{name}  a guide-written interactive page
                    (library/<slug>/artifacts/<name>.html) behind the
                    no-network CSP wall (ARTIFACT_CSP + nosniff); the
                    shelf embeds it in a sandboxed iframe (allow-scripts
                    only — never allow-same-origin). /{slug}/artifacts/
                    {name} is its file-layout twin with the same headers,
                    registered before the static mount so artifact bytes
                    are never served CSP-less. Writes come from the three
                    brains: the claude allowlist (Edit/Write on
                    library/**/artifacts/*.html), the ollama
                    write_artifact tool, and the MCP write_artifact tool —
                    all pinned by artifact_path (slug chars + .html,
                    existing talks only, ~256KB cap).
    GET  /api/history?session=<id>  that session's last 50 completed turns
                    (default: the current/most-recent session) plus its id.
    /<talk>/...     library/ served statically (Range supported, audio seeks)

Chat memory persists under library/.chat/ (private, gitignored with the
rest of library/): each conversation is a session — turns in
sessions/<id>.jsonl, a sidecar sessions/<id>.json holding {id, title,
summary, talks, created, updated, claude_session_id} — and state.json
remembers which session is current. A pre-sessions history.jsonl is folded
into a first "Earlier conversation" session on startup (migrate_history).
When a session is left (a different one gets its first POST), a single
non-streamed call to whichever brain is cheap and up (ollama preferred)
writes its title + two-line summary into the sidecar; failures fall back
to the first user line as the title.

Ambient context: when "view" names a talk, the claude brain's prompt is
prefixed with one line naming that talk and its transcript path, and the
ollama brain's retrieval leads with that talk's chunks — "this talk" and
"what did he say" then mean the thing on screen. Past-tense recall goes
the other way: search_sessions scores keywords over stored turns and
summaries, exposed to the claude brain as `uv run tools/search_history.py`
(allowlisted) and to the ollama brain as the search_history tool.

Chat replies stream as chunked plain text (`text/plain; charset=utf-8`,
one raw text fragment per chunk — no SSE framing; the panel just appends
each decoded chunk with textContent). Failures BEFORE the stream starts
still return JSON {"error": ...} with a 4xx/5xx status; failures MID-stream
degrade gracefully: whatever text already arrived is kept and one plain
error sentence is appended.

Three brains:
    --brain claude (default)  runs the authenticated Claude CLI headless in
        this repo (`--output-format stream-json --include-partial-messages`),
        so it inherits the CLAUDE.md guide persona and can read library/
        itself. Agency is scoped: `--permission-mode default` plus an
        --allowedTools allowlist grant Edit/Write ONLY on STUDY.md,
        journal/**, library/**/notes.md, and library/INDEX.md, Bash ONLY
        for our reviewed tools (fetch_talk.py, speak.py, build_shelf.py,
        search_history.py), plus read-only WebSearch/WebFetch — no
        general Bash, nothing else. That is the safety boundary (see
        chat_allowed_tools).
        Conversation continuity via `--resume <session_id>`, falling back
        to folding trimmed history into the prompt.
    --brain ollama  talks to a local Ollama (http://localhost:11434) with
        "stream": true. Local models are weak rememberers, so the server
        does the remembering: a condensed guide persona + STUDY.md +
        library/INDEX.md + the top keyword-matched transcript chunks are
        packed into the system prompt. <think> blocks are filtered out of
        the stream (ThinkFilter). If the model supports tool calling
        (/api/show capabilities), it gets a bounded agency loop
        (ollama_tool_loop) over the same four reviewed tools the claude
        brain has — fetch_talk, rebuild_shelf, speak, search_history —
        every call passed through validate_tool_call (argv only, no
        shell). Models without tool support degrade gracefully: they are
        told they have no hands and answer normally.
    --brain hermes  talks to the second-arrow hermes-agent profile's
        OpenAI-compatible gateway (http://127.0.0.1:8642). The gateway does
        its own remembering: each episode maps to a gateway session via the
        X-Hermes-Session-Id header (shelf-<episode>), so only the new user
        message (+ the ambient-context prefix) is sent — body history is
        ignored server-side by design. Behind a wired-gate (cached): the
        gateway must be reachable AND expose toolsets ⊆ {mcp-second_arrow,
        clarify} (the same wall as tools/hermes_probe.py, whose logic is
        imported, not duplicated) — an over-provisioned gateway is refused.
        Tool starts stream in as progress lines (hermes.tool.progress SSE
        events); 429 (max_concurrent_runs) retries with backoff and turns
        within one episode are serialized client-side. The shelf DISPLAYS
        Hermes truth (profile, model.default, model_routes) and never
        writes under ~/.hermes.

Run with:
    uv run tools/serve_shelf.py                # then open http://localhost:8765
    uv run tools/serve_shelf.py --brain ollama --ollama-model qwen3

Tests (fastapi/uvicorn are lazy-imported, so plain pytest can load this file):
    uv run --with pytest pytest tools/tests/test_serve_shelf.py -v
"""

