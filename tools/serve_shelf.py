def chunk_transcript(text: str, size: int = CHUNK_SIZE) -> list[str]:
    """Split text into ~size-char chunks on word boundaries. No empty chunks."""
    chunks: list[str] = []
    current: list[str] = []
    length = 0
    for word in text.split():
        current.append(word)
        length += len(word) + 1
        if length >= size:
            chunks.append(" ".join(current))
            current, length = [], 0
    if current:
        chunks.append(" ".join(current))
    return chunks


def _keywords(text: str) -> set[str]:
    return {
        w
        for w in re.findall(r"[a-z']+", text.lower())
        if len(w) > 2 and w not in STOPWORDS
    }


def pick_chunks(
    question: str, transcripts: dict[str, str], k: int = 4
) -> list[tuple[str, str]]:
    """Top-k transcript chunks by keyword overlap with the question.

    transcripts maps talk title -> full transcript text. Returns
    (title, chunk) pairs, best first; chunks with no overlap are dropped.
    """
    terms = _keywords(question)
    scored: list[tuple[int, str, str]] = []
    for title, text in transcripts.items():
        for chunk in chunk_transcript(text):
            score = len(terms & _keywords(chunk))
            if score:
                scored.append((score, title, chunk))
    scored.sort(key=lambda item: -item[0])  # stable: ties keep shelf order
    return [(title, chunk) for _, title, chunk in scored[:k]]


def pick_chunks_for_view(
    question: str,
    transcripts: dict[str, str],
    view_title: str | None,
    k: int = 4,
) -> list[tuple[str, str]]:
    """pick_chunks, but the talk open on the shelf leads the retrieval.

    Its best-matching chunks come first; when the question shares no
    keywords with it at all, its opening chunk still leads (the model must
    know what "this talk" is). No view, or an unknown title, degrades to
    plain pick_chunks.
    """
    if not view_title or view_title not in transcripts:
        return pick_chunks(question, transcripts, k)
    lead = pick_chunks(question, {view_title: transcripts[view_title]}, k=2)
    if not lead:
        opening = chunk_transcript(transcripts[view_title])[:1]
        lead = [(view_title, chunk) for chunk in opening]
    rest = [pair for pair in pick_chunks(question, transcripts, k) if pair not in lead]
    return (lead + rest)[:k]


def ambient_prefix(
    view: str | None,
    titles: dict[str, str],
    session_id: str | None = None,
    listened_last: str | None = None,
) -> str:
    """Prompt lines for what's on screen: the session id and the open talk.

    titles maps slug -> display title (from INDEX.md). The talk line
    teaches the claude brain what "he" and "this talk" refer to; the
    session line tells it which conversation update_session_summary
    should retitle; listened_last (most recent completion, ISO) lets the
    guide bridge naturally toward wrap-up. Any part may be absent.
    """
    lines = []
    if session_id:
        lines.append(f"[session: {session_id}]")
    if view and view in titles:
        completion = (
            f" (The user has listened to this talk to the end — most "
            f"recently {listened_last[:10]}.)"
            if listened_last
            else ""
        )
        lines.append(
            f'[The user currently has "{titles[view]}" open on the shelf — its '
            f"transcript is library/{view}/transcript.md. Treat it as the ambient "
            f'context; "he/this talk" refers to it.{completion}]'
        )
    return "\n".join(lines)


# --- listened: the shelf remembers what finished ------------------------------


def load_listening(path: Path) -> list[dict]:
    """[{slug, at, retract}] from .listening.jsonl — corrupt lines skipped.

    The log is append-only: a listen is {slug, at}; "mark unheard" appends
    a retraction {slug, at, retract: true} instead of rewriting history.
    Readers apply latest-entry-wins (see last_listened).
    """
    if not path.exists():
        return []
    entries: list[dict] = []
    for line in path.read_text(encoding="utf-8").splitlines():
        line = line.strip()
        if not line:
            continue
        try:
            record = json.loads(line)
        except json.JSONDecodeError:
            continue
        if (
            isinstance(record, dict)
            and isinstance(record.get("slug"), str)
            and isinstance(record.get("at"), str)
        ):
            entries.append(
                {
                    "slug": record["slug"],
                    "at": record["at"],
                    "retract": record.get("retract") is True,
                }
            )
    return entries


def last_listened(entries: list[dict], slug: str) -> str | None:
    """The most recent completion stamp for a talk, or None.

    Latest entry wins: a retraction newer than every listen reads as
    not-heard; a listen after a retraction counts again.
    """
    mine = [e for e in entries if e["slug"] == slug]
    if not mine:
        return None
    latest = max(mine, key=lambda e: e["at"])
    return None if latest.get("retract") else latest["at"]


def _append_listening_note(notes_path: Path, line: str) -> None:
    """One quiet line under "## Listening" (heading created once)."""
    text = notes_path.read_text(encoding="utf-8") if notes_path.exists() else ""
    match = re.search(r"## Listening\n+", text)
    if match:
        text = text[: match.end()] + line + "\n" + text[match.end() :]
    else:
        base = text.rstrip("\n")
        text = (base + "\n\n" if base else "") + "## Listening\n\n" + line + "\n"
    notes_path.write_text(text, encoding="utf-8")


def record_listened(library: Path, slug, at: str | None = None) -> bool:
    """A talk finished: log it and note it. Returns False when deduped.

    slug must be an existing talk folder. Completions of the same talk
    within LISTEN_DEDUPE_SECONDS fold into one (refresh loops, doubled
    events). Each real completion appends {slug, at} to .listening.jsonl
    and one line to the talk's notes.md under "## Listening" — both
    private paths the guide already reads.
    """
    if not isinstance(slug, str) or not TALK_SLUG_RE.fullmatch(slug):
        raise ValueError(f"invalid talk slug {slug!r}")
    if not (library / slug).is_dir():
        raise ValueError(f"no talk {slug!r} in the library")
    stamp = at or datetime.now(timezone.utc).isoformat(timespec="seconds")
    log_path = library / ".listening.jsonl"
    previous = last_listened(load_listening(log_path), slug)
    if previous:
        try:
            gap = (
                datetime.fromisoformat(stamp) - datetime.fromisoformat(previous)
            ).total_seconds()
            if 0 <= gap < LISTEN_DEDUPE_SECONDS:
                return False
        except ValueError:
            pass  # a garbled stamp never blocks a real completion
    with log_path.open("a", encoding="utf-8") as handle:
        handle.write(json.dumps({"slug": slug, "at": stamp}) + "\n")
    _append_listening_note(
        library / slug / "notes.md", f"- listened to the end — {stamp[:10]}"
    )
    return True


def mark_listened(library: Path, slug, at: str | None = None) -> dict:
    """The ONE write path behind POST /api/listened — the player's
    automatic completion report and the room's manual "mark as heard"
    button both land here. Records via record_listened (log + notes,
    deduped, ValueError on a bad slug), then rebuilds the shelf page so
    the sidebar's heard ring is already in the next fetch, and returns
    the new state (last completion + the rebuilt page's mtime, which the
    page adopts so the version poll doesn't double-refresh)."""
    recorded = record_listened(library, slug, at=at)
    if recorded:
        rebuild_shelf(library)
    return {
        "ok": True,
        "recorded": recorded,
        "last": last_listened(load_listening(library / ".listening.jsonl"), slug),
        "shelf_mtime": shelf_mtime(library),
    }


def record_unheard(library: Path, slug, at: str | None = None) -> bool:
    """"Mark unheard — come back to this": retract a talk's heard state.

    Appends {slug, at, retract: true} to the append-only log (latest
    entry wins — see last_listened) and one quiet "flagged to revisit"
    line to the talk's notes.md. Returns False when the talk isn't heard
    right now (nothing to retract — idempotent). ValueError on a bad
    slug, same wall as record_listened.
    """
    if not isinstance(slug, str) or not TALK_SLUG_RE.fullmatch(slug):
        raise ValueError(f"invalid talk slug {slug!r}")
    if not (library / slug).is_dir():
        raise ValueError(f"no talk {slug!r} in the library")
    log_path = library / ".listening.jsonl"
    if last_listened(load_listening(log_path), slug) is None:
        return False
    stamp = at or datetime.now(timezone.utc).isoformat(timespec="seconds")
    with log_path.open("a", encoding="utf-8") as handle:
        handle.write(
            json.dumps({"slug": slug, "at": stamp, "retract": True}) + "\n"
        )
    _append_listening_note(
        library / slug / "notes.md", f"- flagged to revisit — {stamp[:10]}"
    )
    return True


def mark_unheard(library: Path, slug, at: str | None = None) -> dict:
    """The write path behind POST /api/unheard — record_unheard, then a
    shelf rebuild so the card and sidebar read not-heard, returning the
    new state the page adopts (mirror of mark_listened)."""
    retracted = record_unheard(library, slug, at=at)
    if retracted:
        rebuild_shelf(library)
    return {
        "ok": True,
        "retracted": retracted,
        "last": last_listened(load_listening(library / ".listening.jsonl"), slug),
        "shelf_mtime": shelf_mtime(library),
    }


